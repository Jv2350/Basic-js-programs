// addition of two numbers

// hard coded values
const num1 = 5;
const num2 = 4;

const sum = num1 + num2;
console.log(`Addition of ${num1} and ${num2} is ${sum} using hardcoded values.`)



// taking inputs from the user
const n1 = parseInt(prompt("Enter 1st number: "));
const n2 = parseInt(prompt("Enter 2nd number: "));

console.log(`Addition of ${n1} and ${n2} is ${n1 + n2} using prompt.`)
